    public static boolean isValuteExist(String valuteValue) {
        List<String> valuteList = new ArrayList<>();
        Arrays.stream(EValute.values()).forEach(v -> valuteList.add(v.toString()));

        return valuteList.contains(valuteValue);
    }

    public static EValute getRandomValute() {
        List<EValute> valuteList = List.of(EValute.values());

        return valuteList.get(new Random().nextInt(valuteList.size()));
    }
